The threee data sets include both spontaneous activity and odor responses.</li>
<li>Purkinje cells recording from rat cerebellar slices. Data obtained and sorted by Matthieu Delscluse.</li>
<li>The earthquakes data set used by Ogata (1988).</li>
</ul> <br>
