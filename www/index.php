<ul>
  <li><big>STAR is an <a href="http://www.r-project.org/">R</a> package
to analyze spike trains. It provides tools to visualize spike trains
and fit, test and compare models of discharge applied to actual data.</big></li>
  <li><big>STAR works with spike train(s) from one neuron or several neurons
simultaneously recorded.<br>
    </big></li>
    <li><big>STAR can perform automatic analysis of spike trains and generate html reports. 
    The following two links show reports generated by a single command of STAR:
    <ul>
  <li><a href="STAR_folder/e070528spontN1.html">In the spontaneous regime</a>. 
  The example of function/method <span style="font-family: monospace;">reportHTML.spikeTrain</span> shows how to generate this report.</li>
  <li><a href="STAR_folder/e070528citronellalN1.html">With repeated stimulations applied</a>.
  The example of function/method <span style="font-family: monospace;">reportHTML.repeatedTrain</span> shows how to generate this report.</li>
  <br>
    </big></li>
 </ul>
 <li><big>STAR runs with the last version of R: R-2.6.0<br>
    </big></li>
    <li><big>A <a href="STAR_folder/PouzatChaffiol_STARdraft.pdf">draft of a manuscript</a> describing the basic functionalities of STAR and including a tutorial is available. Last update: November 13.<br>
    </big></li>
 
 
<br>
<hr style="width: 100%; height: 2px;"><big><span
 style="font-weight: bold;">Features presently implemented in STAR
include</span>:<br>
</big>
<ul>
  <li><big>Instantaneous firing rate estimates. They are obtained with
a convolution of a Gaussian kernel with the spike train.</big></li>
  <li><big>Test of independence of successive inter-spike intervals
(ISIs) in stationary discharge regimes.</big></li>
  <li><big>Fit of an ISI sample from a single neuron in stationary
regime with various models: log-normal, inverse Gaussian, gamma,
Weibull, refractory-exponential, log-logistic. The model parameters are obtained with the
method of maximum likelihood and models are compared with AIC.</big></li>
  <li><big>Test of the adequacy of the best model above.</big></li>
  <li><big>Cross-correlograms of spike trains from different neurons
recorded simultaneously.</big></li>
  <li><big>The time rescaling (<a href="http://www.stat.cmu.edu/~kass/papers/rescaling.pdf" >Brown et al, 2002, Neural Comp. 14: 325</a>) / time transformation (<a href="http://links.jstor.org/sici?sici=0162-1459%28198803%2983%3A401%3C9%3ASMFEOA%3E2.0.CO%3B2-1" >Ogata, 1988, JASA, 83: 9</a>)
is implemented with the full collection of tests of Ogata (1988).
</big></li>
<li><big>The statistical smoothing approach of <a href="http://www.stat.cmu.edu/~kass/papers/smooth.pdf" >Kass, Ventura and Cai (2003) Network: Computation in Neural Systems 14: 5</a>.
But instead of their BARS method a GAM (General Additive Model) based approach with the R package <span style="font-family: monospace;">mgcv</span> of 
Simon Wood is used.
</big></li>
<li><big>The spike-train probability model approach of <a href="http://www.stat.cmu.edu/~kass/papers/imi.pdf"">Kass and Ventura (2001) Neural Comp. 13: 1713</a>.
The job is done again using the GAM based approach of <span style="font-family: monospace;">mgcv</span>.
</big></li>
<li><big>Hidden Markov Models can be used in the spontaneous regime. 
</big></li>
<li><big>A demo reproducing Fig. 2-13 of Ogata (1988) shows how to work with (fully) parametric models. 
</big></li>
</ul>
<br>
